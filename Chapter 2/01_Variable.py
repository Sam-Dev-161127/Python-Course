# VARIABLES IN PYTHON

# Variables: are used to store data in a program. They can hold different types of data such as numbers, text, and more. In Python, you can create a variable by simply assigning a value to it using the equals sign (=).

age = 15 # This line creates a variable named 'age' and assigns it the value of 15. The variable 'age' can now be used to store the age of a person or any other relevant information in the program.

name = "Sam" # This line creates a variable named 'name' and assigns it the string value "Sam". The variable 'name' can be used to store the name of a person or any other relevant information in the program.

marks = 92.5 # This line creates a variable named 'marks' and assigns it the float value 92.5. The variable 'marks' can be used to store the marks of a student or any other relevant information in the program.

# You can also perform operations on variables. For example, you can add two numbers together or concatenate two strings.

# Adding two numbers
num1 = 10 # This line creates a variable named 'num1' and assigns it the value of 10.
num2 = 20 # This line creates a variable named 'num2' and assigns it the value of 20.
sum = num1 + num2 # This line creates a variable named 'sum' and assigns it the result of adding 'num1' and 'num2'. The variable 'sum' will now hold the value 30.

# Concatenating two strings
first_name = "John" # This line creates a variable named 'first_name' and assigns it the string value "John".
last_name = "Doe" # This line creates a variable named 'last_name' and assigns it the string value "Doe".
full_name = first_name + " " + last_name # This line creates a variable named 'full_name' and assigns it the result of concatenating 'first_name', a space, and 'last_name'. The variable 'full_name' will now hold the value "John Doe".   

# You can also change the value of a variable by reassigning it.
age = 16 # This line reassigns the variable 'age' with the new value of 16. The variable 'age' now holds the updated value of 16 instead of 15. 

#Importantly, variable names should be descriptive and follow certain rules. They should start with a letter or an underscore, and can contain letters, numbers, and underscores. Variable names are case-sensitive, meaning that 'age' and 'Age' would be considered different variables.