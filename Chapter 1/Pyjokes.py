# PYJOKES IN PYTHON

# Pyjokes: is a library in Python that provides a collection of programming jokes. It is a fun way to lighten the mood while coding and can be used to add humor to your projects or simply to enjoy some laughs related to programming.

import pyjokes  # Import the pyjokes library for programming jokes

# Get a random programming joke
joke = pyjokes.get_joke() # Retrieve a random joke from the pyjokes library and store it in the variable 'joke'

# Print the joke to the screen
print(joke) # Output the joke stored in the variable 'joke' to the console


# Explanation:
# 1. We imported the pyjokes library, which is a collection of programming jokes.
# 2. We used the get_joke() function from the pyjokes library to retrieve a random joke and stored it in the variable 'joke'.
# 3. We printed the joke to the screen using the print() function.
